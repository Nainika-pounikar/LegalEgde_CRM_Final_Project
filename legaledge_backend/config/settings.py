# Database
db_conn_max_age = 0 if DEBUG else env_int('DB_CONN_MAX_AGE', default=60)
db_ssl_mode = env_str('DB_SSLMODE', default='prefer' if DEBUG else 'require')
allowed_db_ssl_modes = {'disable', 'allow', 'prefer', 'require', 'verify-ca', 'verify-full'}
if db_ssl_mode not in allowed_db_ssl_modes:
    raise ImproperlyConfigured(
        'DB_SSLMODE must be one of: disable, allow, prefer, require, verify-ca, verify-full.'
    )
if not DEBUG and db_ssl_mode != 'require':
    raise ImproperlyConfigured('In production, DB_SSLMODE must be set to "require".')
DATABASE_URL = env_str('DATABASE_URL', default='').strip()
if not DEBUG and not DATABASE_URL:
    raise ImproperlyConfigured('DATABASE_URL environment variable is required in production.')

if DATABASE_URL:
    parsed_db = dj_database_url.parse(DATABASE_URL, conn_max_age=db_conn_max_age)
    parsed_engine = parsed_db.get('ENGINE')
    if not DEBUG and parsed_engine != 'django.db.backends.postgresql':
        raise ImproperlyConfigured('Production DATABASE_URL must point to PostgreSQL.')
    if parsed_engine == 'django.db.backends.postgresql':
        options = dict(parsed_db.get('OPTIONS') or {})
        options.setdefault('sslmode', db_ssl_mode)
        parsed_db['OPTIONS'] = options
    DATABASES = {'default': parsed_db}
else:
    db_engine = env_str('DB_ENGINE', default='sqlite').strip().lower()
    if not DEBUG and db_engine != 'postgres':
        raise ImproperlyConfigured('Production requires PostgreSQL. Set DATABASE_URL or DB_ENGINE=postgres.')
    if db_engine == 'postgres':
        DATABASES = {
            'default': {
                'ENGINE': 'django.db.backends.postgresql',
                'NAME': env_str('DB_NAME'),
                'USER': env_str('DB_USER'),
                'PASSWORD': env_str('DB_PASSWORD'),
                'HOST': env_str('DB_HOST', default='127.0.0.1'),
                'PORT': env_str('DB_PORT', default='5432'),
                'CONN_MAX_AGE': db_conn_max_age,
                'OPTIONS': {
                    'sslmode': db_ssl_mode,
                },
            }
        }
    else:
        DATABASES = {
            'default': {
                'ENGINE': 'django.db.backends.sqlite3',
                'NAME': BASE_DIR / 'db.sqlite3',
            }
        }


