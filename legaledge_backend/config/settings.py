# Process env vars always win over file values.
env_files_in_precedence_order = [
    PROJECT_ROOT / '.env',
    BASE_DIR / '.env',
    PROJECT_ROOT / '.env.local',
    BASE_DIR / '.env.local',
]
for env_path in env_files_in_precedence_order:
    load_env_file(env_path, override=True)

if load_dotenv:
    for env_path in env_files_in_precedence_order:
        load_dotenv(env_path, override=False)

def env_str(name, default=''):
    value = os.environ.get(name, None)
    if value is None:
        return default
    value = str(value).strip()
    return value if value else default


def env_bool(name, default=False):
    value = env_str(name, str(default))
    if isinstance(value, bool):
        return value
    return str(value).strip().lower() in {'1', 'true', 'yes', 'on'}


def env_list(name, default=''):
    value = env_str(name, default)
    return [item.strip() for item in str(value).split(',') if item.strip()]


def env_int(name, default=0):
    raw = env_str(name, default='')
    if raw == '':
        return default
    try:
        return int(raw)
    except (TypeError, ValueError) as exc:
        raise ImproperlyConfigured(f'{name} must be an integer.') from exc


def env_required(name):
    value = env_str(name, default='')
    if not value:
        raise ImproperlyConfigured(f'{name} environment variable is required.')
    return value


def is_strong_secret_key(secret_value):
    if not secret_value:
        return False
    return (
        len(secret_value) >= 50
        and len(set(secret_value)) >= 5
        and not secret_value.startswith('django-insecure-')
    )


